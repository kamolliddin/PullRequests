# Python program to perform +, -, * and / of two integer numbers:
x1 = int(input("Enter First Number: "))
x2 = int(input("Enter Second Number: "))

print("x1 + x2: ", x1 + x2)
print("x1 - x2: ", x1 - x2)
print("x1 * x2: ", x1 * x2)
if x2 == 0:
    print("Cannot devide by zero!")
else:
    print("x1 / x2: ", x1 / x2)
